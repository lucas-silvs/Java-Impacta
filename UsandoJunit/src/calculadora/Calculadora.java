package calculadora;

public  class Calculadora {

	public Double soma(double d, double e) {
		double soma = d+e;
		return soma;
	}
	
	public Double sub(double d, double e) {
		double soma = d-e;
		return soma;
	}

	
	public Double mult(double d, double e) {
		double soma = d*e;
		return soma;
	}

	
	public Double div(double d, double e) {
		double soma = d/e;
		return soma;
	}


}
